package kr.or.ddit.study01;

public class ShortCut {
	public static void main(String[] args) {
		// 단축키 
		// ctrl+shift+l  : 모든 단축키 안내 
		// ctrl+space    : 자동 완성 
		// ctrl+ shift +/ : 범위주석 단축키
		// ctrl+ shift + c :  한줄 주석/취소
		// ctrl+ n : 신규 생성창 
		// ctrl+ a : 전체선택 
		// ctrl+ shift + f : 정렬 
		// ctrl+ alt + 방향키 : 복사
		// ctrl+ d : 삭제
		// alt + 방향키 : 줄 이동. 
		
		// ctrl+ z : 실행 취소 
		// ctrl+ y : 실행 취소의 취소 
		// ctrl+ s : 저장
		// ctrl+ shift +s : 전체 저장
		
		// ctrl+ f11  : 소스 실행 
		// alt + shift + a : 여러줄 입력 
		// alt + shift + r : 변수 여러개 바꾸기
		
		// f11 : 디버깅 실행 
		
		System.out.println("aaa");
		System.out.println("aaa");
		System.out.println("aaa");
		
		
		int rename2 = 0; 
		rename2 = 10; 
		System.out.println(rename2);
		

	}
}
